
void main() {
  //declare
  Object a;

  //fill value as a int
  a = 75;
  print('Nilai a\t :' + a.toString());
  print('Tipe a\t :' + a.runtimeType.toString());

  //fill double type value

  a = 10.5;

  print('\nNilai a\t :' + a.toString());
  print('Tipe a\t :' + a.runtimeType.toString());

  //fill value as a boolean
  a = true;
  print('\nNilai a\t :' + a.toString());
  print('Tipe a\t :' + a.runtimeType.toString());

  //fill value as a string
  a = "Amri";
  print('\nNama saya ' + a.toString());
  print('Tipe a\t :' + a.runtimeType.toString());

}
